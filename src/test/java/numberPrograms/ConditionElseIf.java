package numberPrograms;

public class ConditionElseIf {

	public static void main(String[] args) {
		int x=-20;
		if(x<0) {
			System.out.println("Number is Negative");
		}
		else if(x>0) {
			System.out.println("Number is Positive");
		}
		else {
			System.out.println("Number is Neutral");

		}
	}

}
