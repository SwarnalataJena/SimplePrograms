package numberPrograms;

public class SwitchCase {

	public static void main(String[] args) {
		String str="maggi";
		switch(str) {
		case "chicken" :{
			System.out.println("chicken will be provided");
			break;
		}
		case "chowmin" : {
			System.out.println("chowmin will be provided");
			break;
		}
		case "maggi" : {
			System.out.println("maggi will be provided");
			break;
		}
		case "chapati" : {
			System.out.println("chapati will be provided");
			break;
		}
		default :{
			System.out.println("One cup of tea");

		}
		}

	}

}
